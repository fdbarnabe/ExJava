/*join()  -> Transforma o array em uma string com os itens separado pelo delimitador passado por parametro

para rodar o arquivo = node nomedoargivo.js
*/

const x = ["Fernando", "Domingues", "Barnabe"];

console.log("Valor original: ", x);
console.log("join: ", x.join(" "));

/*
Valor original:  [ 'Fernando', 'Domingues', 'Barnabe' ]
Join:  Fernando Domingues Barnabe
*/