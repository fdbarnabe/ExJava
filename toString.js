/*toString() -> Ele transforma o array em uma string com os itens separados por virgula

para rodar o arquivo = node nomedoargivo.js
*/

const x = ["Fernando", "Domingues", "Barnabe"];

console.log("Valor original: ", x);
console.log("toString: ", x.toString());

/*
Valor original:  [ 'Fernando', 'Domingues', 'Barnabe' ]
toString:  Fernando,Domingues,Barnabe
*/